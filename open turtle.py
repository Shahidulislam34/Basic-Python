# import package 
import turtle 

# forward turtle by 100 

turtle.width(3)
turtle.forward(100) 

# stamp the turtle shape 
turtle.stamp() 

# set the position by using setpos() 
turtle.up() 
turtle.setpos(-50,50) 
turtle.down() 

# forward turtle by 100 
turtle.forward(100) 

# stamp the turtle shape 
turtle.stamp() 

# set the position by using goto() 
turtle.up() 
turtle.goto(-50,-50) 
turtle.down() 

# forward turtle by 100 
turtle.forward(100)


turtle.done()
import turtle
turtle.screensize(canvwidth = 600, canvheight = 600, bg = 'green')
turtle.width(3)

turtle.speed(1)
turtle.circle(2)

turtle.up()
turtle.setpos(50, 0)
turtle.down()

turtle.left(90)
turtle.circle(50)

turtle.up()
turtle.setpos(50, 110)
turtle.down()


turtle.circle(50)
turtle.color('green')
turtle.circle(50)
turtle.color('black')



turtle.done()